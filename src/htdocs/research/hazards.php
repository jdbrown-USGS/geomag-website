<p>
  As part of the Geologic Hazards Science Center, the USGS Geomagnetism Program
  is charged with monitoring the Earth's magnetic field and assessing the ground
  level hazard due to changing fields. Although many space weather effects are
  global due to the interactive nature between the solar wind and the Earth's
  magnetosphere, ground-level effects may be highly localized. Continuing with
  the long-time mapping tradition of the USGS, the Geomagnetism Program is
  developing maps that specify regional hazard conditions during geomagnetic
  disturbances.  This development includes <a href="/conductivity/">regional
  conductivity maps</a>, and magnetic field and electric field estimates.  In
  addition, the local indices described on the <a href="spaceweather.php">Space
  Weather</a> page are being evaluated for potential regional interpolation.
  These products will be made available on the <a href="/map/">USGS Geomagnetism
  Program Hazard Map</a> (beta), as they become available.
</p>

