<h2>A Long History of Service</h2>

<p>
  Over the course of its existence, the USGS Geomagnetism Program has
  evolved in response to changing National needs and organizational changes
  within various Federal agencies. The Program traces its origins back to
  1807 when Congress authorized coastal surveys, including geomagnetic
  surveys, under the Treasury Department. In the 19th century, magnetic
  stations were established to help with the development of magnetic maps
  for the United States and Territories. With the purchase of Alaska,
  coastal surveys became increasingly important, and so in 1878 the Coast
  and Geodetic Survey (CGS) was established, with a separate Division of
  Terrestrial Magnetism established in 1899. The first permanent CGS
  geomagnetic observatory was established in 1900, and because of the
  co-location of seismometers on some observatory sites, a fruitful
  collaboration between Geomagnetism and Seismology began. In 1903, the CGS
  was transferred to the newly established Department of Commerce and
  became part of NOAA when that agency was formed in 1970. Soon thereafter,
  both the Geomagnetism and Seismology Programs in CGS were transferred to
  the USGS.
</p>

<h2>The Program Today</h2>

<p>
  Geomagnetism is headquartered with the USGS Geologic Hazards Team in
  Golden, Colorado, which also includes staff supported by the Earthquake
  Hazards and Landslide Hazards Programs. The engineers and technicians of
  the Geomagnetism Program are responsible for the collection,
  transportation, and dissemination of data from 14 observatories in real
  time, and the program also has significant data-processing and management
  capabilities. Accurate measurements are made automatically at each
  observatory every second. By necessity, everything associated with
  acquiring and handling the data is technologically elaborate, and the
  system's many finely tuned components need to be carefully operated
  and synchronized.
</p>

<h2>Looking to the Future</h2>

<p>
  In response to the rapidly evolving science of geomagnetism, and the ever
  more stringent demands of the scientific community, Program staff are
  constantly upgrading and modernizing the observatory network. In the
  future, the Geomagnetism Program will monitor the magnetic field at
  higher frequencies and, when needed, gradually expand the observatory
  network in collaboration with allied agencies. Program staff are
  undertaking new scientific research and product development focused on
  characterizing the complicated behavior of the Earth's magnetic field in
  space and time, especially during magnetic storms.
</p>
