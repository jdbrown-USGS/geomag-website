<p>
  The mission of the U.S. Geological Survey's Geomagnetism Program is to
  monitor the Earth's magnetic field. Using ground-based observatories, the
  Program provides continuous records of magnetic field variations covering
  long timescales; disseminates magnetic data to various governmental,
  academic, and private institutions; and conducts research into the nature
  of geomagnetic variations for purposes of scientific understanding and
  hazard mitigation. The Program is an integral part of the U.S.
  Government's National Space Weather Program (NSWP), which also includes
  programs in the National Aeronautics and Space Administration (NASA), the
  Department of Defense (DOD), the National Oceanic and Atmospheric
  Administration (NOAA), and the National Science Foundation (NSF). The NSWP
  works to provide timely, accurate, and reliable space weather warnings,
  observations, specifications, and forecasts, and its work is important for
  the U.S. economy and national security.
</p>

<p>
  USGS data are used to model and map the global magnetic field, which
  requires cooperation among the international community of geomagnetism
  and various satellite programs. The USGS Geomagnetism Program's work with
  foreign national geomagnetism programs is coordinated through Intermagnet,
  a worldwide consortium of observatory programs, and the International
  Association of Geomagnetism and Aeronomy. Regionally, USGS data are used
  to support aeromagnetic surveys and directional drilling programs for the
  oil and gas extraction industry. USGS data also are used by the pipeline
  and electrical power grid industries, as well as for academic studies
  across a broad range of geophysical sciences.
</p>

