<h2>Monitoring the Earth's Dynamic Magnetic Field</h2>

<p>
  For centuries, the compass has been used for orientation and
  navigation, with the north-seeking tendency of its magnetized
  needle responding to Earth's magnetic field.  The main part of
  the field is complicated in shape and it changes over time.  As a
  result, magnetic maps and charts need to be updated every few years,
  an on-going project that requires the collection of magnetic data
  from satellites, dedicated surveys, and permanent observatories
  located around the world. In recent years, monitoring of the Earth's
  magnetic field has become important for mitigating the impacts of
  space weather. Magnetic storms, or periods of time when the field is
  unusually active, are caused by the dynamic interaction of the
  Earth's magnetic field with the Sun. It is during magnetic storms
  that beautiful auroral lights can be seen at high latitudes. But
  magnetic storms can also adversely affect the infrastructure and
  activities of our modern, technology based society. Large storms
  can cause the loss of radio communication, reduce the accuracy of
  global-positioning systems, damage satellite electronics and affect
  satellite operations, enhance radiation levels for astronauts and
  high-altitude pilots, increase pipeline corrosion, and induce
  voltage surges in electric power grids, causing blackouts.
  Consequently, the science of geomagnetism is more important today
  than at any point in the discipline's long and colorful history.
</p>

<h2>The USGS Geomagnetism Program</h2>

