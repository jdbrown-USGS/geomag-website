<h2>Public-Private Partnerships</h2>

<p>
  The Deadhorse observatory was built and is operated by Schlumberger with
  guidance from the USGS Geomagnetism Program according to INTERMAGNET
  standards.  Data from this observatory are used to support directional
  drilling for oil on the north shore of Alaska.
</p>

<p>
  Real-time data dissemination software used by the USGS Geomagnetism Program
  was developed in collaboration with the USGS Earthquake Hazards Program and
  Instrumental Software Technologies Inc (ISTI).  The USGS Dst service was
  developed in collaboration with ISTI, with support from Space Enviornment
  Technologies (SET) and the Air Force Research Laboratory (AFRL).
</p>

